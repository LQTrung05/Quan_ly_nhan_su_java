    private void btn_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_saveActionPerformed
        try{
            if(txt_id.getText().equals("")){
                txt_id.requestFocus();
                throw new Exception("Mã chức vụ không được để trống!");
            }
            if(txt_name.getText().equals("")){
                txt_name.requestFocus();
                throw new Exception("Tên chức vụ không được để trống!");
            }
            if(txt_phucap.getText().equals("")){
                txt_phucap.requestFocus();
                throw new Exception("Phụ cấp không được để trống!");
            }
            if(!txt_phucap.getText().matches("[0-9\\.]+")){
                txt_phucap.requestFocus();
                throw new Exception("Phụ cấp phải là số!");
            }
            if(cbo_bacluong.getSelectedIndex()==-1){
                throw new Exception("Vui lòng chọn bậc lương!");
            }
            for(ChucVuModel cv : ls) {
                if(cv.getMaCV().equals(txt_id.getText())){
                    txt_id.requestFocus();
                    throw new Exception("Mã chức vụ này đã tồn tại!");
                }
            }
            
            String maCV = txt_id.getText();
            String tenCV = txt_name.getText();
            Double phuCap = Double.valueOf(txt_phucap.getText());
            int BacLuong = (Integer)cbo_bacluong.getSelectedItem();
            ChucVuModel newcv = new ChucVuModel(maCV, tenCV, phuCap, BacLuong);
            try {
                ls.add(newcv);
                FileOutputStream fs=new FileOutputStream(fileName);
                ObjectOutputStream os=new ObjectOutputStream(fs);
                for(ChucVuModel cv : ls)
                    os.writeObject(cv);
                fs.close();
                os.close();
                
                reLoad();
                cbo_chucvu.setEnabled(true);
                cbo_chucvu.setSelectedIndex(0);
                btn_save.setVisible(false);
                btn_cancel.setVisible(false);
                btn_update.setEnabled(true);
                btn_delete.setEnabled(true);
                btn_add.setEnabled(true);
                txt_id.setEnabled(false);
                JOptionPane.showMessageDialog(null, "Thêm thành công!");
            }
            catch(Exception ex) {
                System.out.println("Lỗi ghi file chức vụ");
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.getMessage(), "Thêm thất bại!", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btn_saveActionPerformed

    private void cbo_chucvuItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbo_chucvuItemStateChanged
        if(cbo_chucvu.getSelectedItem()!=null){
            String choose = ((ComboItem)cbo_chucvu.getSelectedItem()).getValue();
            int count = 0;
            for(NhanSuModel nv : dsnv) {
                if(nv.getMaCV().equals(choose))
                    count++;
            }
            for(ChucVuModel z: ls)
                if(choose.equals(z.getMaCV())) {
                    txt_id.setText(z.getMaCV());
                    txt_name.setText(z.getTenCV());
                    txt_phucap.setText(String.valueOf(String.valueOf(new DecimalFormat("#0").format(z.getPhuCap()))));
                    if(z.getBacLuong()==0)
                        cbo_bacluong.setSelectedIndex(-1);
                    else{
                        cbo_bacluong.setSelectedItem(z.getBacLuong());
                    txt_soluong.setText(String.valueOf(count));
                    txt_soluong.setEnabled(false);
                }
            }
        }
    }//GEN-LAST:event_cbo_chucvuItemStateChanged

    private void btn_updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_updateActionPerformed
        int choose = cbo_chucvu.getSelectedIndex();
        try{
            if(txt_name.getText().equals("")){
                txt_name.requestFocus();
                throw new Exception("Tên chức vụ không được để trống!");
            }
            if(txt_phucap.getText().equals("")){
                txt_phucap.requestFocus();
                throw new Exception("Phụ cấp không được để trống!");
            }
            if(!txt_phucap.getText().matches("[0-9\\.]+")){
                txt_phucap.requestFocus();
                throw new Exception("Phụ cấp phải là số!");
            }
            if(cbo_bacluong.getSelectedIndex()==-1){
                throw new Exception("Vui lòng chọn bậc lương!");
            }
            
            String maCV = txt_id.getText();
            String tenCV = txt_name.getText();
            Double phuCap = Double.valueOf(txt_phucap.getText());
            int BacLuong = (Integer)cbo_bacluong.getSelectedItem();
            ChucVuModel newcv = new ChucVuModel(maCV, tenCV, phuCap, BacLuong);
            
            int index = ls.indexOf(newcv);
            if(index != -1)
                ls.set(index, newcv);
            try {
                FileOutputStream fs=new FileOutputStream(fileName);
                ObjectOutputStream os=new ObjectOutputStream(fs);
                for(ChucVuModel cv : ls)
                    os.writeObject(cv);
                fs.close();
                os.close();
                
                reLoad();
                cbo_chucvu.setSelectedIndex(choose);
                JOptionPane.showMessageDialog(this, "Sửa thành công!");
            }
            catch(Exception ex) {
                System.out.println("Lỗi ghi file chức vụ");
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(this, e.getMessage(), "Sửa thất bại!", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btn_updateActionPerformed

    private void btn_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_deleteActionPerformed
        if(JOptionPane.showConfirmDialog (null, "Bạn chắc chắn muốn xóa bản ghi này?","Bạn chắc chứ?",JOptionPane.YES_NO_OPTION)==JOptionPane.YES_OPTION){
            try{
                int choose = cbo_chucvu.getSelectedIndex();
                String maCV = txt_id.getText();
                String tenCV = txt_name.getText();
                Double phuCap = Double.valueOf(txt_phucap.getText());
                int BacLuong = (Integer)cbo_bacluong.getSelectedItem();
                ChucVuModel xoacv = new ChucVuModel(maCV, tenCV, phuCap, BacLuong);
                int index = ls.indexOf(xoacv);
                if(index != -1) {
                    ls.remove(index);
                }
                else
                    throw new Exception("Không tồn tại chức vụ này!");
                try {
                    FileOutputStream fs=new FileOutputStream(fileName);
                    ObjectOutputStream os=new ObjectOutputStream(fs);
                    for(ChucVuModel cv : ls)
                        os.writeObject(cv);
                    fs.close();
                    os.close();
                    JOptionPane.showMessageDialog(this, "Xoá thành công!");
                    reLoad();
                }
                catch(Exception ex) {
                    System.out.println("Lỗi ghi file chức vụ");
                }
            }
            catch(Exception e){
                JOptionPane.showMessageDialog(this, e.getMessage(), "Xoá thất bại!", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btn_deleteActionPerformed

