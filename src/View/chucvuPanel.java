    public void loadData(){
        btn_save.setVisible(false);
        btn_cancel.setVisible(false);
        
        // Lay danh sach bac luong
        try {
            FileInputStream fi=new FileInputStream(file_bl);
            ObjectInputStream ois=new ObjectInputStream(fi);
            BacLuongModel bl;
            bl_ls = new ArrayList<>();
            while(fi.available() > 0)
            {
                bl = (BacLuongModel) ois.readObject();
                bl_ls.add(bl);
            }
            fi.close();
            ois.close();
            for(BacLuongModel s : bl_ls)
                cbo_bacluong.addItem(s.getBacLuong());
            reLoad();
        } catch (Exception ex) {
             System.out.print("lỗi đọc file bậc lương");
        }
        
        // Lay danh sach nhan vien
        try {
            FileInputStream fi=new FileInputStream(file_nv);
            ObjectInputStream ois=new ObjectInputStream(fi);
            NhanSuModel nv;
            dsnv = new ArrayList<>();
            while(fi.available() > 0)
            {
                nv = (NhanSuModel) ois.readObject();
                dsnv.add(nv);
            }
            fi.close();
            ois.close();
        } catch (Exception ex) {
            System.out.println("Lỗi đọc file nhân sự");
        }
        reLoad();
    }
    
    public void reLoad(){
        try {
            FileInputStream fi=new FileInputStream(fileName);
            ObjectInputStream ois=new ObjectInputStream(fi);
            ChucVuModel cv;
            ls = new ArrayList<>();
            while(fi.available() > 0)
            {
                cv = (ChucVuModel) ois.readObject();
                ls.add(cv);
            }
            fi.close();
            ois.close();
        } catch (Exception ex) {
             System.out.print("lỗi đọc file chức vụ");
        }
        cbo_chucvu.removeAllItems();
        for(ChucVuModel z: ls)
        {
            cbo_chucvu.addItem(new ComboItem(z.getTenCV(),z.getMaCV()));
        }
    }

